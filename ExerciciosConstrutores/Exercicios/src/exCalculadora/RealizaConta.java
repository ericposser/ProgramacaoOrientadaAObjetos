package exCalculadora;

public class RealizaConta {
	public float v1, v2;
	
	public float Soma() {
		float resultado = v1 + v2;
		return resultado;
	}
	
	public float Subtracao() {
		float resultado = v1 - v2;
		return resultado;
	}
	
	public float multiplicacao() {
		float resultado = v1 * v2;
		return resultado;
	}
	
	public float divisao() {
		float resultado = v1 / v2;
		return resultado;
	}
}
